<script>
import foot from './footer'
export default {
  	name: 'product',
	components: {
  	foot
  }
}

$(function(){
	//	鼠标滚动
	$(window).scroll(function(){
		if($(this).scrollTop()>0){
			$('.head').css({'background-color':'#fff','border-bottom':'1px #e5e5e5 solid'})
			$('.psp').css('color','#272727')
			$('.head ul span').css('color','#272727')
			$('.head ul a').css('color','#272727')
		}else{
			$('.head').css({'background-color':'','border-bottom':'1px #ffffff solid'})
			$('.psp').css('color','#fff')
			$('.head ul span').css('color','#fff')
			$('.head ul a').css('color','#fff')
		}
	})
//	nav
	$('.pingtai').mouseover(function(){
		$('.off').css('display','block')
		$('.head').css('background-color','#fff')
 		$('.psp').css('color','#272727')
		$('.head ul span').css('color','#272727')
		$('.head ul li a').addClass('a_ho')
	})
	$('.pingtai').mouseout(function(){
		$('.off').css('display','none')
		$('.head').css('background-color','')
		$('.psp').css('color','#fff')
		$('.head ul span').css('color','#fff')
		$('.head ul li a').removeClass('a_ho')
	})
	
//	体验间
	//	tiyan_control
	var i=0;
	var clone=$(".tiyan_control .img li").first().clone();
	$(".tiyan_control .img").append(clone);	
	var size=$(".tiyan_control .img li").length;	
	for(var j=0;j<size-1;j++){
		$(".tiyan_control .num").append("<li></li>");
	}
	$(".tiyan_control .num li").first().addClass("on");
	/*鼠标划入圆点*/
	$(".tiyan_control .num li").hover(function(){
		var index=$(this).index();
		i=index;//把i和index匹配起来
		$(".tiyan_control .img").stop().animate({left:-index*100+'%'},500)	
		$(this).addClass("on").siblings().removeClass("on")		
	})
	/*向左的按钮*/
	$(".tiyan_control .btn_l").click(function(){
		i++
		move();	
	})
	/*向右的按钮*/
	$(".tiyan_control .btn_r").click(function(){
		i--
		move()				
	})
	function move(){
		if(i==size){
			$(".tiyan_control  .img").css({left:0})			
			i=1;
		}
		if(i==-1){
			$(".tiyan_control .img").css({left:-(size-1)*100+'%'})
			i=size-2;
		}
		$(".tiyan_control .img").stop().animate({left:-i*100+'%'},500)	
		
		if(i==size-1){
			$(".tiyan_control .num li").eq(0).addClass("on").siblings().removeClass("on")	
		}else{		
			$(".tiyan_control .num li").eq(i).addClass("on").siblings().removeClass("on")	
		}		
	}
})

