$(function(){
	//	鼠标滚动
	$(window).scroll(function(){
			if($(this).scrollTop()>0){
				$('.head').css({'background-color':'#fff','border-bottom':'1px #e5e5e5 solid'})
				$('.psp').css('color','#272727')
				$('.head ul span').css('color','#272727')
				$('.head ul a').css('color','#272727')
			}else{
				$('.head').css({'background-color':'','border-bottom':'1px #ffffff solid'})
				$('.psp').css('color','#fff')
				$('.head ul span').css('color','#fff')
				$('.head ul a').css('color','#fff')
			}
	})
//	nav
	$('.pingtai').mouseover(function(){
		$('.off').css('display','block')
		$('.head').css('background-color','#fff')
 		$('.psp').css('color','#272727')
		$('.head ul span').css('color','#272727')
		$('.head ul li a').addClass('a_ho')
	})
	$('.pingtai').mouseout(function(){
		$('.off').css('display','none')
		$('.head').css('background-color','')
		$('.psp').css('color','#fff')
		$('.head ul span').css('color','#fff')
		$('.head ul li a').removeClass('a_ho')
	})
//	banner
	var i=0;
	var clone=$(".banner .img li").first().clone();
	$(".banner .img").append(clone);	
	var size=$(".banner .img li").length;	
	for(var j=0;j<size-1;j++){
		$(".banner .num").append("<li></li>");
	}
	$(".banner .num li").first().addClass("on");
	
	/*鼠标划入圆点*/
	$(".banner .num li").hover(function(){
		var index=$(this).index();
		i=index;//把i和index匹配起来
		$(".banner .img").stop().animate({left:-index*100+'%'},500)	
		$(this).addClass("on").siblings().removeClass("on")		
	})
	
	
	/*自动轮播*/
	var t=setInterval(function(){
		i++;
		move()
	},2000)
	
	
	/*对banner定时器的操作*/
	$(".banner").hover(function(){
		clearInterval(t);
	},function(){
		t=setInterval(function(){
			i++;
			move()
		},2000)
	})
	
//	/*向左的按钮*/
//	$(".banner .btn_l").click(function(){
//		i++
//		move();	
//	})
//	/*向右的按钮*/
//	$(".banner .btn_r").click(function(){
//		i--
//		move()				
//	})
	
	function move(){
		
		if(i==size){
			$(".banner  .img").css({left:0})			
			i=1;
		}
		
		if(i==-1){
			$(".banner .img").css({left:-(size-1)*100+'%'})
			i=size-2;
		}
		
		$(".banner .img").stop().animate({left:-i*100+'%'},500)	
		
		if(i==size-1){
			$(".banner .num li").eq(0).addClass("on").siblings().removeClass("on")	
		}else{		
			$(".banner .num li").eq(i).addClass("on").siblings().removeClass("on")	
		}		
	}

//	鼠标滑过图片变暗
	$('.contain img').hover(function(){
		$(this).animate({
			
		})
	})
	
	//	news
	$('.news_mid div').hover(function(){
		$(this).find('p').first().css({
			color:'#C5A47E'
		})
		$(this).find('p').css({
			fontSize:'1.1vw',
		})
	})
	$('.news_mid div').mouseout(function(){
		$(this).find('p').first().css({
			color:'black'
		})
		$(this).find('p').css({
			fontSize:'0.8vw',
		})
	})

//	图片动态
	$('.zhanlue div').find('img').hover(function(){
		$(this).animate({width:95+'%'})
	})
	$('.zhanlue img').mouseout(function(){
		$(this).animate({width:85+'%'})
	})
	
	$('.cooperate_con div').find('img').hover(function(){
		$(this).animate({top:'-6%'})
	})
	$('.cooperate_con img').mouseout(function(){
		$(this).animate({top:0})
	})
	
})
